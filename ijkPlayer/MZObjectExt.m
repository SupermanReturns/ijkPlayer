CGPoint CGRectGetCenter(CGRect rect)
{
    CGPoint pt;
    pt.x = CGRectGetMidX(rect);
    pt.y = CGRectGetMidY(rect);
    return pt;
}

CGRect CGRectMoveToCenter(CGRect rect, CGPoint center)
{
    CGRect newrect = CGRectZero;
    newrect.origin.x = center.x-CGRectGetMidX(rect);
    newrect.origin.y = center.y-CGRectGetMidY(rect);
    newrect.size = rect.size;
    return newrect;
}

@implementation UIView (MZUIViewExt)

- (UIViewController *)viewController {
    id next = [self nextResponder];
    while (next) {
        next = [next nextResponder];
        if ([next isKindOfClass:[UIViewController class]]) {
            return next;
        }
    }
    return Nil;
}

- (UIViewController *)parentViewController {
    UIViewController *viewController = self.viewController;
    if (viewController) {
        return [self superController:viewController];
    } else return nil;
}

- (UIViewController *)superController:(UIViewController *)viewController {
    UIViewController *superVc = viewController.parentViewController;
    if (superVc) {
        [self superController:superVc];
    } else return viewController;
    return nil;
}

- (NSArray *)allSubviews {
    NSArray *results = [self subviews];
    for (UIView *eachView in [self subviews])
    {
        NSArray *riz = [eachView allSubviews];
        if (riz) results = [results arrayByAddingObjectsFromArray:riz];
    }
    return results;
}

- (void)removeAllSubviews {
    for (UIView *subview in self.allSubviews) {
        [subview removeFromSuperview];
    }
}

@end

